/*
 * Calculate the remainder of the data passed through a 24-bit CRC.
 */
uint32_t crc_24(uint8_t *data, size_t data_len)
{
	/* The actual CRC remainder is stored in the top 3 bytes of crc. The least
	   significant byte holds the next byte of the message to be shifted
	   through the CRC. */
	uint32_t crc = 0;
	int i;

	for (; data_len > 0; data_len--)
	{
		/* Set up the next byte in the holding area... */
		crc |= (*data++);

		/* ...and shift it through the CRC (assuming an 8-bit byte). */
		for (i = 0; i < 8; i++)
			if (crc & (1 << (sizeof(crc) * 8 - 1)))
				crc = (crc << 1) ^ (CRC_POLY << 8);
			else
				crc = (crc << 1);
	}

	/* Extract the remainder (assuming an 8-bit byte). */
	return (crc >> 8);
}

